// OPERADORES PARA  NUMEROS
3 + 2; // operador de suma
50 - 10; // operador de resta
10 * 20; // operador de multiplicación
20 / 2; // operador de división

// CONCATENACIÓN DE STRINGS
"Alfonso" + " Moran";

// OPERADOR DE NEGACIÓN
!false; // Devuelve verdadero
!true; // Devuelve falso

// OPERADOR DE ASIGNACIÓN
var a = 1;
var b = 2;

//OPERADOR DE COMPARACIÓN
3 == "3"; // verdadero; compara valores iguales
3 === "3" // falso; compara valores y tipo de datos
5 > 3; // mayor que
5 < 3; // menor que
5 <= 6; // menor o igual que
5 >= 6; //  mayor o igual que

a && b; // AND
a || b; // OR

var edad = 40;
edad++ // edad = 41, ++ incremente en uno el valor
edad += 2; // edad = 43, += incremente el valor de la variable edad según lo indicado después del iguañ
